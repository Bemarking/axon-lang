#  Mathematical-purity boundary (founder ratification 2026-05-08)
# ════════════════════════════════════════════════════════════════════


class TestPurityBoundary:
    """Compute blocks must not be a back door for PIX / MDN / effects.

    The DSL grammar enforces this by construction: only let/return,
    only the four arithmetic operators, only known identifiers. These
    tests assert that the obvious back doors are closed."""

    @pytest.fixture
    def t(self):
        return CTranspiler()

    @pytest.mark.parametrize("forbidden_op", [
        "navigate", "drill", "trail", "recall", "record",
        "perform", "handle", "resume", "abort", "forward",
        "memory", "corpus", "pix",
    ])
    def test_pix_mdn_effects_keywords_rejected_as_identifiers(
        self, t, forbidden_op,
    ):
        # These are not in the param list nor declared as locals, so
        # the transpiler treats them as unknown identifiers and
        # rejects them. The grammar boundary holds even if an adopter
        # accidentally pastes one in.
        with pytest.raises(ValueError, match="Unknown identifier"):
            t.transpile(f"return {forbidden_op}", "Forbidden", [])

    def test_pure_arithmetic_passes_boundary(self, t):
        # The boundary blocks impure stuff; pure morphisms over f64
        # are explicitly the supported case.
        result = t.transpile(
            "let x = a * a\nlet y = b * b\nreturn x + y",
            "Norm2Squared",
            ["a", "b"],
        )
        assert "axon_compute_Norm2Squared" in result.fn_name
